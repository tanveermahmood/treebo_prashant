package com.treebo_comp.Bean;

public class ProductInfoBean {
	
	public String hotelName;
	public String hotelPrice;
	public String hotelLocation;
	public String hotelDiscount;
	
	public String getHotelName() {
		return hotelName;
	}
	public void setHotelName(String productName) {
		this.hotelName = productName;
	}
	public String getHotelPrice() {
		return hotelPrice;
	}
	public void setHotelPrice(String productPrice) {
		this.hotelPrice = productPrice;
	}
	public String getHotelLocation() {
		return hotelLocation;
	}
	public void setHotelLocation(String productName) {
		this.hotelLocation = productName;
	}
	public String getHotelDiscount() {
		return hotelDiscount;
	}
	public void setHotelDiscount(String productPrice) {
		this.hotelDiscount = productPrice;
	}

}
